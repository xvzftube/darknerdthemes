
#' nerd Color Palettes
#'
#' Functions to create color palettes from nerd theme colors
#' @param n Number of colors required
#' @name nerd_palettes
#' @details Color palettes built from popular dark nerd themes
#' @examples
#' nerd_main(10)
#'
#' @rdname nerd_palettes
#' @export
nerd_main <- nerd_pal()

#' @rdname nerd_palettes
#' @export
red_hard <- nerd_pal(palette = "red hard")

#' @rdname nerd_palettes
#' @export
nord_frost <- nerd_pal(palette = "nord frost")

#' @rdname nerd_palettes
#' @export
nord_aurora <- nerd_pal(palette = "nord aurora")

#' @rdname nerd_palettes
#' @export
everforest <- nerd_pal(palette = "everforest")
